public class Role  {

    public Role(Integer idRole, String roleName, Users idUser) {
        this.idRole = idRole;
        this.roleName = roleName;
        this.idUser = idUser;
    }


    private Integer idRole;
   
    private String roleName;
 
    private String skills;
    
    private String experience;
    
    private String entreprise;
   
    private String langageDeProgrammation;

    private Users idUser;

    public Role() {
    }

    public Role(Integer idRole) {
        this.idRole = idRole;
    }

    public Integer getIdRole() {
        return idRole;
    }

    public void setIdRole(Integer idRole) {
        this.idRole = idRole;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public String getSkills() {
        return skills;
    }

    public void setSkills(String skills) {
        this.skills = skills;
    }

    public String getExperience() {
        return experience;
    }

    public void setExperience(String experience) {
        this.experience = experience;
    }

    public String getEntreprise() {
        return entreprise;
    }

    public void setEntreprise(String entreprise) {
        this.entreprise = entreprise;
    }

    public String getLangageDeProgrammation() {
        return langageDeProgrammation;
    }

    public void setLangageDeProgrammation(String langageDeProgrammation) {
        this.langageDeProgrammation = langageDeProgrammation;
    }

    public Users getIdUser() {
        return idUser;
    }

    public void setIdUser(Users idUser) {
        this.idUser = idUser;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (idRole != null ? idRole.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Role)) {
            return false;
        }
        Role other = (Role) object;
        if ((this.idRole == null && other.idRole != null) || (this.idRole != null && !this.idRole.equals(other.idRole))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entites.Role[ idRole=" + idRole + " ]"+roleName +idUser ;
    }
    
}
